<?php
$x = 10;

// Incrementing the value of $x by 1
$x--;

echo "Incremented value: $x"; // Outputs: Incremented value: 11

// Apa perbedaan antara increment dan decrement?
/*
Increment adalah operasi yang menambahkan 1 ke nilai variabel, sedangkan decrement adalah operasi yang mengurangi 1 dari nilai variabel.
*/
?>

